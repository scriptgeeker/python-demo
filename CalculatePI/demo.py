# 精确至小数点100位
num = 100

# 根据马青公式计算圆周率
b = 10 ** (num + 10)  # 多计算10位
x1 = 16 * b // 5  # 求含16/5的首项
x2 = -4 * b // 239  # 求含4/239的首项
pi = x1 + x2  # 求第一大项
for i in range(3, num * 2, 2):
    x1 //= -5 ** 2  # 求每个含1/5的项及符号
    x2 //= -239 ** 2  # 求每个含1/239的项及符号
    pi += (x1 + x2) // i  # 计算两项之和

pi = str(pi)  # 将PI当字符串处理
pi = pi[0] + '.' + pi[1:-10]

print(pi)
